package com.hummingbird.maaccount.entity;

import com.hummingbird.maaccount.face.Account;

/**
 * @author john huang
 * 2015年2月9日 下午3:16:33
 * 本类主要做为 线下支付订单基类
 */
public class DefaultOfflinePayOrder<T extends Account> extends AbstractOrder<T> {

	
	protected String shiftInfo;
	protected String storeId;
    protected String operatorId;
    protected String terminalId;
    protected String terminalOrderId;
    protected String sellerId;
    protected String batchNo;
    protected Long productPrice;
    /**
     * 商品数量
     */
    protected String productQuantity;
    
    
    /**
	 * @return the storeId
	 */
	public String getStoreId() {
		return storeId;
	}

	/**
	 * @param storeId the storeId to set
	 */
	public void setStoreId(String storeId) {
		this.storeId = storeId;
	}

	/**
	 * @return the operatorId
	 */
	public String getOperatorId() {
		return operatorId;
	}

	/**
	 * @param operatorId the operatorId to set
	 */
	public void setOperatorId(String operatorId) {
		this.operatorId = operatorId;
	}

	/**
	 * @return the terminalId
	 */
	public String getTerminalId() {
		return terminalId;
	}

	/**
	 * @param terminalId the terminalId to set
	 */
	public void setTerminalId(String terminalId) {
		this.terminalId = terminalId;
	}

	/**
	 * @return the sellerId
	 */
	public String getSellerId() {
		return sellerId;
	}

	/**
	 * @param sellerId the sellerId to set
	 */
	public void setSellerId(String sellerId) {
		this.sellerId = sellerId;
	}

	/**
	 * @return the terminalOrderId
	 */
	public String getTerminalOrderId() {
		return terminalOrderId;
	}

	/**
	 * @param terminalOrderId the terminalOrderId to set
	 */
	public void setTerminalOrderId(String terminalOrderId) {
		this.terminalOrderId = terminalOrderId;
	}

	/**
	 * @return the batchNo
	 */
	public String getBatchNo() {
		return batchNo;
	}

	/**
	 * @param batchNo the batchNo to set
	 */
	public void setBatchNo(String batchNo) {
		this.batchNo = batchNo;
	}

	/**
	 * @return the productQuantity
	 */
	public String getProductQuantity() {
		return productQuantity;
	}

	/**
	 * @param productQuantity the productQuantity to set
	 */
	public void setProductQuantity(String productQuantity) {
		this.productQuantity = productQuantity;
	}

	/**
	 * @return the productPrice
	 */
	public Long getProductPrice() {
		return productPrice;
	}

	/**
	 * @param productPrice the productPrice to set
	 */
	public void setProductPrice(Long productPrice) {
		this.productPrice = productPrice;
	}

	/**
	 * @return the shiftInfo
	 */
	public String getShiftInfo() {
		return shiftInfo;
	}

	/**
	 * @param shiftInfo the shiftInfo to set
	 */
	public void setShiftInfo(String shiftInfo) {
		this.shiftInfo = shiftInfo;
	}
}
