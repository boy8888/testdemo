package com.hummingbird.maaccount.vo;

import com.hummingbird.maaccount.entity.User;
import com.hummingbird.maaccount.face.Account;

/**
 * @author john huang
 * 2015年8月23日 上午10:08:41
 * 本类主要做为
 */
public class AccountUserVO {

	public AccountUserVO(Account account, User user) {
		super();
		this.account = account;
		this.user = user;
	}
	private Account account;
	private User user;
	/**
	 * @return the account
	 */
	public Account getAccount() {
		return account;
	}
	/**
	 * @param account the account to set
	 */
	public void setAccount(Account account) {
		this.account = account;
	}
	/**
	 * @return the user
	 */
	public User getUser() {
		return user;
	}
	/**
	 * @param user the user to set
	 */
	public void setUser(User user) {
		this.user = user;
	}
	/* (non-Javadoc)
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "AccountUserVO [account=" + account + ", user=" + user + "]";
	}
	
}
