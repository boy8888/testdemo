package com.hummingbird.maaccount.entity;

/**
 * 微信充值上限表,控制总的充值上限
 */
public class WxRechargeLimit {
    /**
     * 类型,DAY每日,MON每月,WEEK每周
     */
    private String type;

    /**
     * 限额值,单位为分
     */
    private Integer moneyLimit;

    /**
     * @return 类型,DAY每日,MON每月,WEEK每周
     */
    public String getType() {
        return type;
    }

    /**
     * @param type 
	 *            类型,DAY每日,MON每月,WEEK每周
     */
    public void setType(String type) {
        this.type = type == null ? null : type.trim();
    }

    /**
     * @return 限额值,单位为分
     */
    public Integer getMoneyLimit() {
        return moneyLimit;
    }

    /**
     * @param moneylimit 
	 *            限额值,单位为分
     */
    public void setMoneyLimit(Integer moneyLimit) {
        this.moneyLimit = moneyLimit;
    }
}