package com.hummingbird.maaccount.vo;


/**
 * @author huangjiej_2
 * 2015年1月10日 上午1:56:29
 * 本类主要做为 查询的订单结果输出
 */
public class OrderDetailOutputVO extends OrderdetailOutputBaseVO {

	
	protected String peerAccountId;
	
	protected String peerAccountUnit;
	
	protected String peerAccountType;
	
	/**
	 * 线下交易流水号，比如用户在柜台转账或网银转账产生的交易流水号
	 */
	protected String externalOrderId;
	
	/**
	 * 线下交易时间，用户输入精确到天，提交时前端页面自动补上时分秒，默认为12:00:00
	 */
	protected String externalOrderTime;
	
	protected String type;
	
	/**
	 * 结存，即发生该交易后，该账户的结存
	 */
	protected long balance;
	
	/**
	 * 钱流向,IN#流入,OUT流出
	 */
	protected String flowDirection;

	protected String productName;


	/**
	 * @return the peerAccountId
	 */
	public String getPeerAccountId() {
		return peerAccountId==null?"":peerAccountId;
	}

	/**
	 * @param peerAccountId the peerAccountId to set
	 */
	public void setPeerAccountId(String peerAccountId) {
		this.peerAccountId = peerAccountId;
	}

	/**
	 * @return the peerAccountUnit
	 */
	public String getPeerAccountUnit() {
		return peerAccountUnit==null?"":peerAccountUnit;
	}

	/**
	 * @param peerAccountUnit the peerAccountUnit to set
	 */
	public void setPeerAccountUnit(String peerAccountUnit) {
		this.peerAccountUnit = peerAccountUnit;
	}

	/**
	 * @return the peerAccountType
	 */
	public String getPeerAccountType() {
		return peerAccountType==null?"":peerAccountType;
	}

	/**
	 * @param peerAccountType the peerAccountType to set
	 */
	public void setPeerAccountType(String peerAccountType) {
		this.peerAccountType = peerAccountType;
	}

	/**
	 * @return the externalOrderId
	 */
	public String getExternalOrderId() {
		return externalOrderId==null?"":externalOrderId;
	}

	/**
	 * @param externalOrderId the externalOrderId to set
	 */
	public void setExternalOrderId(String externalOrderId) {
		this.externalOrderId = externalOrderId;
	}

	/**
	 * @return the externalOrderTime
	 */
	public String getExternalOrderTime() {
		return externalOrderTime==null?"":externalOrderTime;
	}

	/**
	 * @param externalOrderTime the externalOrderTime to set
	 */
	public void setExternalOrderTime(String externalOrderTime) {
		this.externalOrderTime = externalOrderTime;
	}

	/**
	 * @return the type
	 */
	public String getType() {
		return type==null?"":type;
	}

	/**
	 * @param type the type to set
	 */
	public void setType(String type) {
		this.type = type;
	}

	/**
	 * @return the balance
	 */
	public long getBalance() {
		return balance;
	}

	/**
	 * @param balance the balance to set
	 */
	public void setBalance(long balance) {
		this.balance = balance;
	}

	/**
	 * @return the flowDirection
	 */
	public String getFlowDirection() {
		return flowDirection;
	}

	/**
	 * @param flowDirection the flowDirection to set
	 */
	public void setFlowDirection(String flowDirection) {
		this.flowDirection = flowDirection;
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "OrderDetailOutputVO [peerAccountId=" + peerAccountId
				+ ", peerAccountUnit=" + peerAccountUnit + ", peerAccountType="
				+ peerAccountType + ", externalOrderId=" + externalOrderId
				+ ", externalOrderTime=" + externalOrderTime + ", type=" + type
				+ ", balance=" + balance + ", flowDirection=" + flowDirection
				+ ", productName=" + productName + ", orderId=" + orderId
				+ ", sum=" + sum + ", remark=" + remark + ", status=" + status
				+ ", insertTime=" + insertTime + ", method=" + method
				+ ", methodName=" + methodName + ", appId=" + appId
				+ ", appName=" + appName + ", appOrderId=" + appOrderId + "]";
	}

	/**
	 * @return the productName
	 */
	public String getProductName() {
		return productName==null?"":productName;
	}

	/**
	 * @param productName the productName to set
	 */
	public void setProductName(String productName) {
		this.productName = productName;
	}

	
	
	
	
	
}
